package headfirstjava.chapter2;

public class IfTest {
    public static void main(String[] args) {
        System.out.println("Using conditional statements in java");
        int x = 3;
        if (x == 3) {
            System.out.println("X is 3.");
        } else {
            System.out.println("X is not 3");
        }
        System.out.println("This will run irrespective");
    }
}
